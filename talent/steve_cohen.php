    <h1>
        <a href="behind_the_scenes/indy.php" class="anchor" name="steve">Steve Cohen</a>
    </h1>
        <p class="date">Founder and Chairman</p>
        <hr/>
        <img class="float_left" src="/_img/talent_steve_cohen.jpg" alt="talent_steve_cohen" width="250" />
        <p>In his professional career Steve Cohen has successfully worn almost every hat imaginable in video, television, and commercial production.</p>
<p>A five time Emmy Award winner, Steve has been the recipient of dozens of other industry awards including the Promax , Tellys, Cine Eagles and more.</p>
<p>In 1982 Steve helped establish NBA Entertainment's reputation as a top production entity and in 1985 was nominated for his first (and NBAE's first) Sports Emmy award for producing and editing a profile about Magic Johnson.</p>

<p>From that point, he spent the next 12 years conquering New York's live television market and worked in a DGA capacity as an associate director, floor director, and director on literally thousands of live telecasts.  Working at the three major networks and virtually every local station in the New York metropolitan area, he received 5 Emmy awards and  7 other nominations during that time.  Some of his credits included The NFL Today, The Olympic Games, The CBS Evening News with Dan Rather, Late Night with David Letterman,  Good Morning America, and local telecasts of every professional New York sports team including the New York Yankees, The New York Mets, The New York Knicks, and the New York Rangers.</p>
<p>People began to see Steve as more than just a director and relied on his vision for creating new on-air looks from the director's chair.   In 1990 he was hired as one of the original directors that launched CNBC and a few years later directed the tests that helped launch Court TV. </p><p>Steve was at the forefront when high definition broadcasting began. He directed live Major League Baseball games for Japanese broadcaster NHK, several years before High definition broadcasts hit the airwaves in the United States.</p>

<p>In 1997 Steve started to follow a different calling and began to transition out of televised broadcasts into becoming a director and director of photography in field acquisition.  Steve's creative vision in that role played a major part in helping to dramatically change the look of sports television's feature profiles and teasers.  As early as 1990, he helped to create a formula that changed the texture of interlaced video footage into film-like images, a feat that was achieved a full 13 years before the advent of 24p video cameras.</p>

<p>In 2004, Steve was specially selected by FOX Sports to be the lead DP on the networks 3D technology testing.  Steve collaborated with Academy Award winner Vince Pace (Avatar, Hanna Montana-Miley Cyrus Best of Both Worlds, U2) on a variety of major sporting events including the NFC Championship Game,  an NBA game, a NASCAR NEXTEL Cup race, and professional boxing. Those initial tests have led to the biggest technological breakthrough in broadcasting since color television.</p>

<p>In 2007 Steve co-created and executive produced "The Speed Road Tour Challenge", a 13 part reality series for Speed TV.  Working closely with Saatchi and Saatchi and utilizing strategic brand placement, the series played a major role in Toyota's breakthrough into the world of Nascar.  No automaker outside of the "Big 3" had ever been able to break in to the sport previously.</p>

<p>Steve established Manhattan Place Entertainment in 1987 and, through his vast experience and extensive industry contacts,  has brought together an extraordinarily talented group of individuals who specialize in virtually every area of the production and creative services industry.</p>

<p>Steve has successfully positioned Manhattan Place Entertainment as a direct source for production and creative services with the unique capability of providing exceptionally high production quality without exceptionally high pricing.</p>

<p>He has been a member of the Directors Guild of America in good standing since 1985 and is also affiliated with IATSE Local 600.</p>
